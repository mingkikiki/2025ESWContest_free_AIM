void led_blink_heartbeat()
{
    static uint32_t last_tick = 0;
    static uint8_t led_on = 0;

    if (HAL_GetTick() - last_tick >= 500) {
        last_tick = HAL_GetTick();
        led_on = !led_on;
        HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, led_on ? GPIO_PIN_SET : GPIO_PIN_RESET);
    }
}
